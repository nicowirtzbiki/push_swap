#include "libft.h"

void    ft_bzero(void *s, size_t n)
{
    size_t  i;
    unsigned char   *ptr;

    if (!s)
        return ;
    i = 0;
    ptr = (unsigned char *)s;
    while (i < n)
    {
        ptr[i] = 0;
        i++;
    }
}